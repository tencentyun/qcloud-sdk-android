package com.tencent.cos.xml.model.ci.ai;

import com.tencent.qcloud.qcloudxml.annoation.XmlBean;
import com.tencent.qcloud.qcloudxml.annoation.XmlElement;

import java.util.List;

@XmlBean(name = "Response", method = XmlBean.GenerateMethod.FROM)
public class LivenessRecognitionResponse {
    
    /**
     * 验证通过后的视频最佳截图照片，照片为 BASE64 编码后的值，JPG 格式注意：此字段可能返回 null，表示取不到有效值
     */
    public String bestFrameBase64;
    /**
     * 相似度，取值范围 [0.00, 100.00]。推荐相似度大于等于70时可判断为同一人，可根据具体场景自行调整阈值（阈值70的误通过率为千分之一，阈值80的误通过率是万分之一）检测成功返回
     */
    public float sim;
    /**
     * 最佳截图列表，仅在配置了返回多张最佳截图时返回注意：此字段可能返回 null，表示取不到有效值
     */
    @XmlElement(flatListNote = true, ignoreListNote = true)
    public List<String> bestFrameList;

}
